import Cocoa

/// 表示通知横幅的类型，用于决定显示的图标和样式。
public enum LCNotificationType {
    
    /// 成功类型
    case success
    
    /// 错误类型
    case error
    
    /// 信息类型
    case info
    
    /// 警告类型
    case warning
}
